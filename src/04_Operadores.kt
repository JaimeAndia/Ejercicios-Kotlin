fun main(){

    var a = 7
    var b = 5

    println("($a + $b) = ${a + b}") //Con el $ se recupera el valor de la variable
    println("(a + b) = ${a + b}")
    println("($a - $b) = ${a - b}")
    println("($a / $b) = ${a / b}")
    println("($a * $b) = ${a * b}")
    println("($a % $b) = ${a % b}")
}